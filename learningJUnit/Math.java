package learningJUnit;
public class Math {
        static public int add(int a, int b) {  
                return a + b;
        }
        
        static public int subtract(int a, int b) {  
            return a - b;
        }
        
        static public int divide(int a, int b){
        	return a / b;
        }
        
        static public int remainder (int a, int b){
        	return a % b;
        }
        
} 